/**
 * Recursively widen every string-literal type to `string`, so
 * translations can use any string value at each key while still
 * being enforced to have the same shape.
 */
type DeepStringify<T> = {
  [K in keyof T]: T[K] extends string ? string : DeepStringify<T[K]>;
};

export type Messages = DeepStringify<typeof en>;
